    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        
        Object[] data = new Object[3];
        data[0] = nama.getText();
        data[1] = note.getText();
        data[2] = kota.getSelectedItem().toString();
        model.addRow(data);
    }//GEN-LAST:event_simpanActionPerformed

    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        nama.setEnabled(true);
        note.setEnabled(true);
        kota.setEnabled(true);
        
        simpan.setEnabled(true);
        update.setEnabled(true);
        hapus.setEnabled(true);
        
        tambah.setEnabled(false);
    }//GEN-LAST:event_tambahActionPerformed
public void InitTable(){
        model = new DefaultTableModel();
        model.addColumn("NAMA");
        model.addColumn("NO TELEPON");
        model.addColumn("KOTA");
        txttabel.setModel(model);
        
    }
    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        int baris = txttabel.getSelectedRow();
        
        txttabel.setValueAt(nama.getText(), baris, 0);
        txttabel.setValueAt(note.getText(), baris, 1);
        txttabel.setValueAt(kota.getSelectedItem(), baris, 2);
        
       simpan.setEnabled(true);
    }//GEN-LAST:event_updateActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        kota.requestFocus();
    }//GEN-LAST:event_hapusActionPerformed

