    private void txttabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txttabelMouseClicked
        // TODO add your handling code here:
        int baris = txttabel.getSelectedRow();
        
        String nama_ed = txttabel.getValueAt(baris, 0).toString();
        String note_ed = txttabel.getValueAt(baris, 1).toString();
        String kota_ed = txttabel.getValueAt(baris, 2).toString();
        
        nama.setText(nama_ed);
        note.setText(note_ed);
        kota.setSelectedItem(kota_ed);
    }//GEN-LAST:event_txttabelMouseClicked

